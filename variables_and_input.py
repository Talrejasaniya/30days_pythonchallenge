# Day 2: Read and process integer, float, and string input, then print their sums and concatenation

i1 = int(input())
d1 = float(input())
s1 = str(input())

# Predefined variables
i = 4
d = 4.0
s = 'HackerRank '

# Print the sum of integers
print(i + i1)

# Print the sum of doubles
print(round(d + d1, 1))

# Print the concatenated strings
print(s + s1)
